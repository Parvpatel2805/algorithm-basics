// Algorithm: Insertion Sort
// Type: Sorting (Comparison-based)
// Time Complexity: O(n^2)
// Space Complexity: O(1)

public static void insertionSort(int[] arr) {
    for (int i = 1; i < arr.length; i++) {
        int key = arr[i];
        int j = i - 1;
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j--;
        }
        arr[j + 1] = key;
    }
}
